CREATE TABLE Company (
	CID			INTEGER,
	Cname 		TEXT NOT NULL,
	NIF			INTEGER UNIQUE NOT NULL,
	address		TEXT NOT NULL,
	CONSTRAINT company_pk PRIMARY KEY (CID)
);
CREATE TABLE Document (
	docID		INTEGER PRIMARY KEY,
	dNAME		TEXT NOT NULL,
	date_ini	DATE NOT NULL,
	date_fin	DATE,
	type		TEXT,
	ownerID		INTEGER,
	CONSTRAINT document_type_fk FOREIGN KEY (type) REFERENCES Type(TID) ON DELETE CASCADE,
	CONSTRAINT document_owner_fk FOREIGN KEY (ownerID) REFERENCES Worker(WID) ON DELETE SET NULL,
	CONSTRAINT checkDiDf CHECK(date_ini<date_fin OR date_fin=NULL)
);
CREATE TABLE Language (
	LID			INTEGER,
	Lname		TEXT NOT NULL,
	CONSTRAINT language_pk PRIMARY KEY (LID)
);
CREATE TABLE File (
	FID			INTEGER,
	docID		INTEGER NOT NULL,
	Fname		TEXT,
	size		INTEGER,
	extension	TEXT,
	CONSTRAINT file_pk PRIMARY KEY (FID), 
	CONSTRAINT file_doc_fk FOREIGN KEY (docID) REFERENCES Document(docID) ON DELETE CASCADE,
	CONSTRAINT checkFSize CHECK(size >=0)
);
CREATE TABLE DocAttribute (
	DAID		INTEGER,
	DAname		TEXT NOT NULL,
	DAvalue		TEXT,
	CONSTRAINT docAttribute_pk PRIMARY KEY (DAID) 
);
CREATE TABLE Type (
	TID			INTEGER,
	Tname		TEXT NOT NULL,
	last_mod_date	DATE,
	status		TEXT,
	remarks		TEXT,
	CONSTRAINT type_pk PRIMARY KEY (TID)
);
CREATE TABLE Worker (
	WID			INTEGER PRIMARY KEY,
	Wname		TEXT NOT NULL
);
CREATE TABLE CompanyRole (
	CRID		INTEGER,
	CRname		TEXT NOT NULL,
	CONSTRAINT companyRole_pk PRIMARY KEY (CRID)
);
CREATE TABLE Revision (
	docID		INTEGER NOT NULL,
	revID		INTEGER PRIMARY KEY,
	authorID	INTEGER,
	version		TEXT,
	date		DATE NOT NULL,
	CONSTRAINT revision_author_fk FOREIGN KEY (authorID) REFERENCES Worker(WID) ON DELETE SET NULL,
	CONSTRAINT revision_doc_fk FOREIGN KEY (docID) REFERENCES Document(docID) ON DELETE CASCADE
);
CREATE TABLE RevisionAttribute (
	RAID		INTEGER,
	RAname		TEXT NOT NULL,
	RAvalue		TEXT,
	CONSTRAINT revisionAttribute_pk PRIMARY KEY (RAID)
);
CREATE TABLE Stated (
	LID			INTEGER,
	docID		INTEGER,
	CONSTRAINT stated_fk1 FOREIGN KEY (LID) REFERENCES Language(LID) ON DELETE CASCADE,
	CONSTRAINT stated_fk2 FOREIGN KEY (docID) REFERENCES Document(docID) ON DELETE CASCADE,
	CONSTRAINT stated_pk PRIMARY KEY(LID,docID)
);
CREATE TABLE DocPossesses (
	DAID		INTEGER,
	docID		INTEGER,
	CONSTRAINT docPossesses_fk1 FOREIGN KEY (DAID) REFERENCES DocAttribute(DAID) ON DELETE CASCADE,
	CONSTRAINT docPossesses_fk2 FOREIGN KEY (docID) REFERENCES Document(docID) ON DELETE CASCADE,
	CONSTRAINT docPossesses_pk PRIMARY KEY(DAID,docID)
);
CREATE TABLE RevPossesses (
	RAID 		INTEGER,
	revID		INTEGER,
	CONSTRAINT revPossesses_fk1 FOREIGN KEY (RAID) REFERENCES RevisionAttribute(RAID) ON DELETE CASCADE,
	CONSTRAINT revPossesses_fk2 FOREIGN KEY (revID) REFERENCES Revision(revID) ON DELETE CASCADE,
	CONSTRAINT revPossesses_pk PRIMARY KEY(revID,RAID)
);
CREATE TABLE Belongs (
	WID			INTEGER,
	CRID		INTEGER,
	CONSTRAINT belongs_fk1 FOREIGN KEY (WID) REFERENCES Worker(WID) ON DELETE CASCADE,
	CONSTRAINT belongs_fk2 FOREIGN KEY (CRID) REFERENCES CompanyRole(CRID) ON DELETE CASCADE,
	CONSTRAINT belongs_pk PRIMARY KEY(WID,CRID)
);
CREATE TABLE Modified (
	TID			INTEGER,
	WID			INTEGER,
	CONSTRAINT modified_fk1 FOREIGN KEY (TID) REFERENCES Type(TID) ON DELETE CASCADE,
	CONSTRAINT modified_fk2 FOREIGN KEY (WID) REFERENCES Worker(WID) ON DELETE CASCADE,
	CONSTRAINT modified_pk PRIMARY KEY(TID,WID)
);
CREATE TABLE Request (
	docID		INTEGER,
	CID			INTEGER NOT NULL,
	SID			INTEGER NOT NULL,
	propose_date	DATE NOT NULL,
	CONSTRAINT request_fk1 FOREIGN KEY (docID) REFERENCES Document(docID) ON DELETE CASCADE,
	CONSTRAINT request_fk2 FOREIGN KEY (CID) REFERENCES Company(CID) ON DELETE CASCADE,
	CONSTRAINT request_fk3 FOREIGN KEY (SID) REFERENCES Status(SID) ON DELETE RESTRICT, --impede que elimine a tabela status caso hajam requests com status atribuido (não sei bem se precisamos disto)
	CONSTRAINT request_pk PRIMARY KEY(docID)
);
CREATE TABLE ProjectRole (
	PRID		INTEGER,
	PRname		TEXT,
	CONSTRAINT projectRole_pk PRIMARY KEY(PRID)
);
CREATE TABLE DocumentRoleWorker (
	docID		INTEGER NOT NULL,
	WID			INTEGER NOT NULL,
	PRID		INTEGER NOT NULL,
	CONSTRAINT drw_document_fk FOREIGN KEY (docID) REFERENCES Document(docID) ON DELETE CASCADE,
	CONSTRAINT drw_role_fk FOREIGN KEY (WID) REFERENCES Worker(WID) ON DELETE CASCADE,
	CONSTRAINT drw_worker_fk FOREIGN KEY (PRID) REFERENCES ProjectRole(PRID) ON DELETE CASCADE
);
CREATE TABLE CheckOut (
	revID		INTEGER,
	WID			INTEGER,
	-- COID		INTEGER,
	COdate		DATE NOT NULL,
	CONSTRAINT checkOut_fk1 FOREIGN KEY (revID) REFERENCES Revision(revID) ON DELETE CASCADE,
	CONSTRAINT checkOut_fk2 FOREIGN KEY (WID) REFERENCES Worker(WID) ON DELETE CASCADE,
	CONSTRAINT checkOut_pk PRIMARY KEY(revID)
);
